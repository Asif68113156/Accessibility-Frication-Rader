@admin.register(ResolutionUpdate)
class ResolutionUpdateAdmin(admin.ModelAdmin):
    list_display = ('report', 'officer', 'colored_rating', 'is_verified_by_citizen', 'resolved_at')
    list_filter = ('is_verified_by_citizen', 'citizen_rating')
    readonly_fields = ('citizen_rating', 'citizen_feedback', 'is_verified_by_citizen')

    def colored_rating(self, obj):
        if not obj.citizen_rating: return "Unrated"
        color = "green" if obj.citizen_rating >= 4 else "orange" if obj.citizen_rating >= 3 else "red"
        return format_html('<span style="color: {}; font-weight: bold;">{} ★</span>', color, obj.citizen_rating)
    colored_rating.short_description = 'Citizen Rating'

