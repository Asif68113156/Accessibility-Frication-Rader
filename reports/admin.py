class ReportMediaInline(admin.TabularInline):
    model = ReportMedia
    extra = 1
    readonly_fields = ('media_preview',)

    def media_preview(self, obj):
        if obj.file:
            return format_html('<img src="{}" style="max-height: 100px;"/>', obj.file.url)
        return "No media"

@admin.register(Report)
class ReportAdmin(admin.ModelAdmin):
    list_display = ('id', 'colored_status', 'title', 'city', 'severity', 'priority_score', 'sla_warning', 'reporter', 'created_at')
    list_filter = ('status', 'severity', 'category', 'city', 'state')
    search_fields = ('title', 'description', 'reporter__username', 'city')
    inlines = [ReportMediaInline]
    actions = ['verify_reports', 'resolve_reports', 'recalculate_priority']
    
    fieldsets = (
        ('General Information', {
            'fields': ('title', 'description', 'category', 'severity', 'status')
        }),
        ('Location Details', {
            'fields': ('address', 'city', 'state', ('latitude', 'longitude')),
            'classes': ('collapse',)
        }),
        ('Professional Analytics (AI)', {
            'fields': ('priority_score', 'ai_summary', 'qr_code'),
            'description': 'Data generated by Gemini AI and Geofence Analysis'
        }),
        ('Meta Data', {
            'fields': ('reporter', 'is_anonymous', 'escalation_level', 'next_escalation_at', 'sla_deadline', 'is_escalated'),
            'classes': ('collapse',)
        }),
    )
    readonly_fields = ('priority_score', 'ai_summary', 'qr_code')

    def colored_status(self, obj):
        colors = {
            'pending': 'orange',
            'verified': 'blue',
            'in_progress': 'purple',
            'resolved': 'green',
            'rejected': 'red'
        }
        return format_html(
            '<span style="color: {}; font-weight: bold;">{}</span>',
            colors.get(obj.status, 'black'),
            obj.get_status_display()
        )
    colored_status.short_description = 'Status'

    def sla_warning(self, obj):
        from django.utils import timezone
        if obj.status != 'resolved' and obj.sla_deadline and obj.sla_deadline < timezone.now():
            return format_html('<span style="color: red; font-weight: bold;">🔴 OVERDUE</span>')
        return obj.sla_deadline.strftime('%d %b') if obj.sla_deadline else "No SLA"
    sla_warning.short_description = 'SLA Deadline'

    def get_queryset(self, request):
        qs = super().get_queryset(request)
        if request.user.is_superuser or request.user.role == 'superadmin':
            return qs
        if request.user.role in ['admin', 'officer']:
            if request.user.city:
                return qs.filter(city__iexact=request.user.city)
            elif request.user.state:
                return qs.filter(state__iexact=request.user.state)
        return qs

    def verify_reports(self, request, queryset):
        queryset.update(status='verified')
        self.message_user(request, "Selected reports have been verified.")
    verify_reports.short_description = "Mark reports as Verified Barrier"

    def resolve_reports(self, request, queryset):
        queryset.update(status='resolved')
        self.message_user(request, "Selected reports have been resolved.")
    resolve_reports.short_description = "Mark reports as Resolved"

    def recalculate_priority(self, request, queryset):
        for rep in queryset:
            rep.save() 
        self.message_user(request, "Priority scores recalculated successfully.")
    recalculate_priority.short_description = "Recalculate Priority Scores"

@admin.register(InfrastructureQRCode)
class InfrastructureQRCodeAdmin(admin.ModelAdmin):
    list_display = ('location_name', 'entity_type', 'health_score', 'qr_preview')
    list_filter = ('entity_type',)
    actions = ['generate_qrs']
    
    def generate_qrs(self, request, queryset):
        from .services import QRCodeService
        for infra in queryset:
            QRCodeService.generate_for_infrastructure(infra)
        self.message_user(request, "QR Codes generated specifically for public auditing.")
    generate_qrs.short_description = "Generate Official Public Audit QRs"
    
    def qr_preview(self, obj):
        if obj.qr_code_image:
            return format_html('<img src="{}" style="max-height: 50px;"/>', obj.qr_code_image.url)
        return "N/A"

